package com.moons.xst.track.xstinterface;

import android.content.DialogInterface;

/**
 * @author LKZ
 * 
 */
public interface SimpleRadioListViewDialogListener {
	/**
	 * 回调函数，刷新父UI
	 */
	public void refreshParentUI();

	/**
	 * 回调函数，返回父UI
	 */
	public void goBackToParentUI();

	/**
	 * 回调函数，确定返回
	 * 
	 * @param mDJPlan
	 */
	public void btnOK(DialogInterface dialog);
	/**
	 * 回调函数，返回
	 * 
	 * @param mDJPlan
	 */
	public void btnCancel(DialogInterface dialog);
}
