package com.moons.xst.track.bean;

import java.io.Serializable;

public class DateEntity implements Serializable {

	int year;
	String month;
	String day;
	String week;

	public DateEntity() {
		super();
	}

	public DateEntity(int year, String month, String day, String week) {
		super();
		this.year = year;
		this.month = month;
		this.day = day;
		this.week = week;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

}
