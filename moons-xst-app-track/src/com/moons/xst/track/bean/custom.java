package com.moons.xst.track.bean;

public class custom {
	Boolean quickState;
	int quickorder;
	String tag;
	
	public custom() {
		super();
	}
	public custom(Boolean quickState, int quickorder, String tag) {
		super();
		this.quickState = quickState;
		this.quickorder = quickorder;
		this.tag = tag;
	}
	public Boolean getQuickState() {
		return quickState;
	}
	public void setQuickState(Boolean quickState) {
		this.quickState = quickState;
	}
	public int getQuickorder() {
		return quickorder;
	}
	public void setQuickorder(int quickorder) {
		this.quickorder = quickorder;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	
}
