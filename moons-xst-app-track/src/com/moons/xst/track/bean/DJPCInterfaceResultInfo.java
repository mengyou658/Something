package com.moons.xst.track.bean;

public class DJPCInterfaceResultInfo {
	private String DJPCItemID;
	private String DJPCItemCD;
	private String DJPCItemTX;
	private String DJPCItemTransTX;
	private boolean IsSelected = false;

	public DJPCInterfaceResultInfo() {
		super();
	}

	public DJPCInterfaceResultInfo(String dJPCItemID, String dJPCItemCD,
			String dJPCItemTX, String dJPCItemTransTX, boolean isSelected) {
		super();
		DJPCItemID = dJPCItemID;
		DJPCItemCD = dJPCItemCD;
		DJPCItemTX = dJPCItemTX;
		DJPCItemTransTX = dJPCItemTransTX;
		IsSelected = isSelected;
	}

	public boolean getIsSelected() {
		return IsSelected;
	}

	public void setIsSelected(boolean isSelected) {
		IsSelected = isSelected;
	}

	public String getDJPCItemID() {
		return DJPCItemID;
	}

	public void setDJPCItemID(String dJPCItemID) {
		DJPCItemID = dJPCItemID;
	}

	public String getDJPCItemCD() {
		return DJPCItemCD;
	}

	public void setDJPCItemCD(String dJPCItemCD) {
		DJPCItemCD = dJPCItemCD;
	}

	public String getDJPCItemTX() {
		return DJPCItemTX;
	}

	public void setDJPCItemTX(String dJPCItemTX) {
		DJPCItemTX = dJPCItemTX;
	}

	public String getDJPCItemTransTX() {
		return DJPCItemTransTX;
	}

	public void setDJPCItemTransTX(String dJPCItemTransTX) {
		DJPCItemTransTX = dJPCItemTransTX;
	}

}
